
package tutorial.spec;

public class Calculator {

    public static strictfp double sumWithfp(double value1, double value2) {
        return value1 + value2;
    }

    public static strictfp double diffWithfp(double value1, double value2) { 
        return value1 - value2; 
    }

    public static double sum(double value1, double value2) {
        return value1 + value2;
    }

    public static double diff(double value1, double value2) { 
        return value1 - value2; 
    }

}

